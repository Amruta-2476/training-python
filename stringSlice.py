'''
positive(L to R) and negative(R to L) indexing
'''
name = 'prashantjha'
print(name[0]) # p
print(name[1]) # r
print(name[-1]) #a
# print(name[15])
print(name[0:5])  # prash
print(name[1:])  # rashantjha
print(name[:5]) # prash
print(name[:]) # prashantjha
print(name[1:8:2])  # rsta
print(name[::-1]) # for reversing the string ahjtnahsarp